//! End-to-end v128-on-JIT tests (D-460 v128-GC field round-trips + D-461 SIMD
//! register-spill correctness). Split from `runner_gc_test.zig` (P1 spec-defined
//! sub-language — v128/SIMD lane + load ops under the JIT — + P3 independent
//! change cadence: the D-461 spill-awareness fixtures evolve on their own track
//! and kept pushing `runner_gc_test.zig` toward the 2000-line hard cap,
//! mirroring the earlier `runner_test.zig → runner_gc_test.zig` split, ADR-0198).
//! Every fixture force-spills a v128 (≥9 simultaneously-live on x86_64's 8 XMM,
//! or struct/array.new pressure) and reads a known lane back, so a non-spill-aware
//! handler surfaces `UnsupportedOp`. Driven through the JIT entry
//! (`runI32Export`). Discovered via `src/zwasm.zig`'s `test {}` block. wat2wasm
//! 1.0.40 predates gc textual support, so module bytes are hand-encoded.

